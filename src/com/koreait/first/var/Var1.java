// 변수타입 설명
public class Var1 {
	public static void main(String[] args) {
		// Primitive 타입 (원시형 타입) / 일반변수타입 라고 하기도 합니다.	-> 소문자로 시작함, 값을 저장한다.
		// 정수형,  실수형, 불린형, 문자형
		
		// 정수형
		byte d1;	// 1byte	(8bit)	-128~127	| 00000000
		short d2;	// 2byte	(16bit)	0000000000000000
		int d3;		// 4byte
		long d4;	// 8byte
		
		// 실수형
		float f1;	// 4byte
		double f2;	// 8byte
		
		// 불린형
		boolean b1;	// true or false
		
		// 문자형
		char c1;
		
		// 대문자로 시작하는 것은 주소값을 저장한다.
	}
}
