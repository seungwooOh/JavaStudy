package com.koreait.first.control;

public class If2 {
	public static void main(String[] args) {
		
		int num = 1;
		
		// num 값이 10 이상이면 "num은 10이상입니다."
		// 10미만이면 "num은 10미만 입니다."
		
		if(num >= 10) {
			System.out.println(num + " 은 10 이상 입니다.");
		} else {
			System.out.println(num + " 은 10미만입니다.");
		}
		
		
		
	}
}
